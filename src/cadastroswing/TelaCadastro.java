    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODObtnExcluir.addActionListener(e -> {
            int linhaSelecionada = tabelaResultados.getSelectedRow(); // Pega a linha selecionada
            if (linhaSelecionada != -1) { // Verifica se alguma linha foi selecionada
                modelo.removeRow(linhaSelecionada); // Remove a linha selecionada
                System.out.println("Linha excluída.");
            } else {
                JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
         
    }//GEN-LAST:event_btnExcluirActionPerformed

