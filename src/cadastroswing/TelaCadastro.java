    private void btnSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar1ActionPerformed
                String valnome = txtCPF.getText();
                String valcpf = txtCPF.getText();
        

                  // Validação do nome
                if (valnome.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "O campo Nome é obrigatório.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
                    txtNome.setBackground(Color.PINK);
                    return;
                } else {
                    txtNome.setBackground(Color.WHITE);
                }

                // Validação do CPF
                if (!valcpf.matches("\\d{11}")) {
                    JOptionPane.showMessageDialog(this, "O CPF deve conter exatamente 11 dígitos numéricos.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
                    txtCPF.setBackground(Color.PINK);
                    return;
                } else {
                    txtCPF.setBackground(Color.WHITE);
                }
        
        
        
        
        JOptionPane.showMessageDialog(this, "Salvo com Sucesso!","Eita !",JOptionPane.INFORMATION_MESSAGE);                        // TODO add your handling code here:
    }//GEN-LAST:event_btnSalvar1ActionPerformed

