    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
 // Pega a linha selecionada
        int linhaSelecionada = tabelaResultados.getSelectedRow();
        if (linhaSelecionada != -1) {  // Verifica se uma linha foi selecionada
            DefaultTableModel model = (DefaultTableModel) tabelaResultados.getModel();  // Obtém o modelo da tabela
            // Atualiza os valores da linha selecionada
            model.setValueAt("Novo Nome", linhaSelecionada, 0); // Atualiza a coluna 0 (Nome)
            model.setValueAt("00000000000", linhaSelecionada, 1); // Atualiza a coluna 1 (CPF)
            System.out.println("Linha atualizada.");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para atualizar.", "Eita!!", JOptionPane.WARNING_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnAtualizarActionPerformed

